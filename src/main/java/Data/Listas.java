package Data;

import java.util.ArrayList;

public class Listas {

    private ArrayList<String> a, b, c, d;

    public Listas(ArrayList<String> a, ArrayList<String> b, ArrayList<String> c, ArrayList<String> d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public ArrayList<String> getA() {
        return a;
    }

    public void setA(ArrayList<String> a) {
        this.a = a;
    }

    public ArrayList<String> getB() {
        return b;
    }

    public void setB(ArrayList<String> b) {
        this.b = b;
    }

    public ArrayList<String> getC() {
        return c;
    }

    public void setC(ArrayList<String> c) {
        this.c = c;
    }

    public ArrayList<String> getD() {
        return d;
    }

    public void setD(ArrayList<String> d) {
        this.d = d;
    }
    
}
